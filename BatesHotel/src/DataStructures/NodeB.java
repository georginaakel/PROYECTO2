public class NodeB {
    private int data;
    private NodeB left;
    private NodeB right;

    public NodeB(int data) {
        this.data = data;
        this.left = left;
        this.right = right;
    }

    public int getData() {
        return data;
    }

    public void setData(int data) {
        this.data = data;
    }

    public NodeB getLeft() {
        return left;
    }

    public void setLeft(NodeB left) {
        this.left = left;
    }

    public NodeB getRight() {
        return right;
    }

    public void setRight(NodeB right) {
        this.right = right;
    }
       
}
